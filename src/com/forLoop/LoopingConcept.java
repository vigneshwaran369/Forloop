package com.forLoop;

public class LoopingConcept {

	public static void main(String[] args) {
   
		for(int i=0;i<3;i++)
			System.out.print("3");
		     System.out.println();
		for(int j=0;j<2;j++)
			System.out.print("2");
		System.out.println();
		for(int k=0;k<1;k++)
			System.out.println("1");
		System.out.println();
	}

}
