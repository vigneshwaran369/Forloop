package com.forLoop;

public class LoopingConcepts1 {

	public static void main(String[] args) {
			
		for(int i=0;i<5;i++) {
			    System.out.print("5");
		    System.out.println();
			for(int j=0;j<4;j++)
				System.out.print("4");
			System.out.println();
			for(int k=0;k<3;k++)
				System.out.print("3");
			System.out.println();
			for(int l=0;l<2;l++ )
				System.out.print("2");
			System.out.println();
			for(int m=0;m<1;m++)
				System.out.print("1");
			System.out.println();
		}
	}

}
