package com.forLoop;

public class Looping {
	
	public static void main(String[] vicky) {
		
		for(int i=0;i<=8;i++)
			System.out.print(" * ");
		System.out.println();
		for(int j=0;j<=4;j++)
			System.out.println("*                         *");
			
		for(int k=0;k<=8;k++)
			System.out.print(" * ");
		System.out.println();
		
	}

}
