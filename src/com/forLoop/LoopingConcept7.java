package com.forLoop;

public class LoopingConcept7 {

	public static void main(String[] args) {
    
		
		for(int i=0;i<4;i++) {
			for(int j=1;j<=i;j++)
				System.out.print(i);
		System.out.println();

		}
       
	
		
		for(int i=3;i>0;i--) {
			for(int j=1;j<=i;j++) {
				System.out.print(i);
		
			}System.out.println();

		
	}

}
}
